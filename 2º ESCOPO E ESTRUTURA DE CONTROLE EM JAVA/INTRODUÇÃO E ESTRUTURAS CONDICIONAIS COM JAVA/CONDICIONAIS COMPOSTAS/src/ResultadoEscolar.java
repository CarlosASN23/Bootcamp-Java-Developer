public class ResultadoEscolar {
    public static void main(String[] args) {

        /*
         * Algumas vezes, o nosso programa deverá seguir mais
         * de uma jornada de execução, condionado a uma regra
         * de negócio, este cenário é demoninado Estrutura Condicional Composta.
         */
        
       int nota = 6;
       
       if(nota >= 7)
        System.out.println("Aprovado");

       else
        System.out.println("Reprovado");
    }
}